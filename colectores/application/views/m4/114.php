   <h2><span class="label">3.-</span> Instrumentos de Testeo de  Drogas con fines laborales
</h2>
  <p>Hoy el estándar internacional, para detectar la  presencia de drogas de forma no invasiva, en una persona, se realiza a través  del análisis de una muestra de orina.<br />
    <br />
    El Programa Drug Free Workplace, considera y ha  definido como estándar los dispositivos <strong>EZ</strong> de Global Partners, que son copas para la recolección de orina, con cierre  hermético, que además están provistas de  tiras reactivas para la detección de las  principales drogas de abuso en ambientes laborales, que permiten al colector  realizar un examen <strong>SIN </strong>manipulación  de la muestra. <br />
    <br />
    Las drogas de control más frecuentes son las Benzodiacepinas,  Opiáceos, Anfetaminas, Metanfetaminas, Marihuana y Cocaína. Equipo EZ Split  Key-Cup.</p>
