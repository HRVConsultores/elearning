<p><strong>El alcotest no marca resultados</strong><br />
  <em>Revisar conexión eléctrica o estado de las pilas. Utilice siempre las boquillas propias del equipo. Si el problema persiste, reemplazar el equipo o enviar al servicio técnico.</em><br />
  </p>
<p><strong>El alcotest no imprime</strong><br />
  <em>Revise si tiene papel suficiente o si existen problemas con el cable o conexión inalámbrica. Siempre apague y vuelva a encender el equipo y pruebe nuevamente. Si persiste, envié el equipo al servicio técnico.</em><br />
  </p>
<p><strong>Resultados fuera de rangos</strong><br />
  <em>Para efectuar un test debe pedir a la persona que no esté comiendo o mascando alimentos o chicles. Si fuera este el caso, deberá pedir a la persona que elimine el alimento, deberá enjuagarse con agua la boca, espera entre 3 a 5 minutos y repetir el test. Si la persona uso alcohol gel antes de realizar el test de alcohol, pida que se lave las manos y enseguida tome el test. Otro factor que puede influir en el resultado, es el estado de las pilas o baterías del equipo, estas siempre deben estar cargadas idealmente a su máxima capacidad para que el sensor funcione correctamente.</em><br />
   <br />
  Dentro de los errores típicos en el proceso, se encuentran principalmente los asociados con el registro en los documentos de testeo, tales como el no registrar la hora del examen, no anotar la confirmación de un resultado por parte del donante. <br />
  Otro error frecuente es la discordancia entre firmas en documentos. <em><strong>En este punto hay que tener especial cuidado, ya que muchas veces el donante de la muestra realiza la firma de forma diferente a la que aparece en la cédula de identidad y para así desconocer el resultado ante el RPE o tribunales si las instancias así lo determinan.</strong><strong></strong></em></p>
