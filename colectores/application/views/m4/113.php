   <h2><span class="label">3.-</span> Instrumentos De Testeo De Alcohol Con Fines Laborales</h2>
  <p>Para la realización de un test de A&D de acuerdo al programa DFWP debemos contar con kits o productos que cumplan con las características que a continuación describiremos: </p>
  <p> Hoy el  estándar internacional, señala como lo más utilizado para medir la cantidad de  alcohol en la sangre de una persona, a los <strong>equipos  digitales de detección de alcohol por flujo de aire expirado, con tecnología de  celda de combustión, </strong>por ser una técnica no invasiva y de gran precisión.<br />
Popularmente  se conocen por el nombre de alcotest de celda de combustión y se utilizan con fines  profesionales, laborales o para el control de adicciones. Estos equipos normalmente  van acompañados de un sistema de impresión y/o de captura de datos, lo que  permite dejar una evidencia del control realizado a un trabajador.<br />
<br />
Estos equipos  tienen la capacidad de detectar los vapores de alcohol existentes en la  expiración de un individuo, proveniente del intercambio gaseoso que se produce  en los pulmones. Lo que luego en base a un algoritmo de cálculo, el software  del equipo es capaz de realizar una extrapolación a la concentración de alcohol  en la sangre&nbsp; - %BAC o en gr/lt.<br />
<br />
Entre los  equipos top de línea podemos encontrar marcas como Lifeloc de EEUU y Sentech de  Korea, con sus modelos F20 y ALP-1 respectivamente.</p>
