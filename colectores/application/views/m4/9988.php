<h2>Síndrome de vejiga tímida: </h2>

<p>Es un trastorno psicológico que consiste en la dificultad de miccionar (orinar) en baños públicos o lugares donde haya otras personas. Bajo estas circunstancias, debemos entregar el máximo de privacidad a la persona testeada, pero  dado que ingresará solo al baño, deberemos tomar algunas medidas de seguridad para evitar que la persona nos adultere la muestra, como por ejemplo, no entrando con chaquetas al baño, revisando bultos extraños bajo su ropa, pidiéndole que saque sus pertenencias de sus bolsillos, etc.</p>
