  <h2>Protocolo de Testeo</h2>
  <strong>¿Qué hacer si se detecta adulteración de la orina?</strong><br /><br />
<p><img src="<?php echo base_url().IMAGES_U; ?>m03/017.png" width="250" height="248" class="foto_der_borderless" />Si  el termómetro no marca la temperatura correcta, el volumen no alcanza el mínimo  requerido o las cintas de adulteración muestran alguna anormalidad, deberá  repetir el test: </p>
<ol>
  <li>Explicar  al postulante o trabajador que los mecanismos de control del dispositivo,  indican que hubo una probable adulteración de la muestra de orina, teniéndose  que repetir el test. <br />
    <br />
  </li>
  <li>La segunda toma de muestra, será obtenida bajo observación, es decir, el Colector  entrará al baño con el donante. <br />
    Ante esta situación el postulante o trabajador <strong>no  debe abandonar las dependencias de testeo</strong>. <br />
    <br />
  </li>
  <li>En  el<strong> Formulario N°3</strong>,  Registro de aplicación de test con Cadena de Custodia. Deberá anotarse que la  temperatura estuvo fuera de rango y/o que las cintas de adulteración mostraron  una posible adulteración de la muestra. También deberá anotarse que se tomará  una segunda muestra bajo observación.<br />
  </li>
</ol>
