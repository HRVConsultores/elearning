  <p class="textoNormalboldSubrayado"><img src="<?php echo base_url().IMAGES_U; ?>m03/033.png" width="287" height="321" class="foto_der_borderless" />Resultado "No testeado":
    </p>
  <p>En caso de que el postulante o trabajador no quiera: realizarse el test; firmar los documentos requeridos o firme como "NO autorizando"; repetir una muestra observada por sospecha de adulteración; no es capaz de entregar una muestra de volumen suficiente para ser analizada, etc., se dará por finalizado el proceso, siendo el resultado del mismo <strong>"No testeado"</strong>. 
    </p>
  <p>Dicha situación deberá ser informada en forma inmediata 
    al RPE y eventualmente a la persona responsable del 
    reclutamiento en caso de testeo pre ocupacional. 
    Se deberá documentar la decisión del postulante y el contacto 
    con el RPE (nombre /cargo) en el Formulario N°3 de Registro 
    de aplicación de test, especificando la hora y algún antecedente 
    relevante de lo conversado en el ítem observaciones.
    
    En el Formulario N°3 "Registro de Aplicación del Test con CDC" se deberá señalar frente a cada droga considerada y alcohol el resultado "<strong>No testeado</strong>".
  </p>
 
