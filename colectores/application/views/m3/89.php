   <h2>Negativo para Alcohol: <br /></h2>
<p>Para efectos de evaluar los resultados de la prueba de alcohol, el "Programa Drug Free Workplace" define, de acuerdo a la experiencia internacional de testeo laboral, que un resultado<em><strong> es negativo</strong></em>, cuando el resultado es menor o igual a 0,020% BAC ó 0,200 gramos por litro de alcohol en sangre. Para efectos de realizar el testeo de alcohol, se usara un equipo de celda de combustión que entrega resultados a la milésima de % de BAC o gr/litro.
 </p>
<ul>
  <li>La boleta impresa emitida por el equipo deberá registrar lo siguiente:    
    <ul>
      <li>Nº correlativo del registro de testeo.</li>
      <li> Iníciales  y firma del colector.</li>
      <li> Iníciales del donante.</li>
    </ul>
  </li>
  <li>Se da oficialmente por negativo con la firma y huella dactilar del postulante o trabajador y la del Colector en el documento "Registro de aplicación del Test con CDC" quedando la boleta del test de alcohol <strong>"corcheteada"</strong> a este documento.
    
    <br />
    <br />
    <img src="<?php echo base_url().IMAGES_U; ?>m03/022.png" width="900" height="349" /></li>
  <li>En el &quot;Registro de Aplicación del Test con CDC&quot; se deberá señalar frente a alcohol el resultado &quot;Negativo&quot; o &quot;NEG&quot;, indicando el porcentaje de BAC o gr/litro que registra el resultado impreso del testeo. </li>
</ul>
 
