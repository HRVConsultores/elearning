<p>Previo a la realización del test de alcohol, el Colector deberá revisar en primer lugar lo siguiente:</p>
<ol>
  <li>Que las baterías del equipo estén en buen estado.<br />
    </li>
  <li>Que el equipo  tenga  la fecha y hora correctas, es decir, que sea coincidente con el momento en que se está  realizando el test.<br />
    </li>
  <li>Verificar que la fecha de calibración no exceda un período máximo de 6 meses.<br />
  </li>
</ol>
<p>Mientras se procesa el test de drogas, el Colector puede realizar el test de alcohol solicitando que el postulante o trabajador sople en la boquilla del equipo de detección de alcohol. <br />
  <br />
  <br />
</p>
