<p><strong>En esta clasificación se distinguen tres niveles:</strong> </p>
<table width="928" border="0" cellpadding="4" cellspacing="4">
  <tr>
    <td width="33%" valign="top"><div class="alert alert-error" style="width:260px">
  <h4 style="font-weight:bold">Prevención universal: </h4>
  <p style="color:#b94a48;">El grupo destinatario de estas intervenciones será la población general de la empresa, sin distinguir cargos, tipos de consumidores o áreas de riesgos.<br />
    <br />
  </p>
</div></td>
    <td width="33%" valign="top"><div class="alert alert-error" style="width:260px">
  <h4 style="font-weight:bold">Prevención selectiva:  </h4>
  <p style="color:#b94a48;">Los destinatarios serán grupos determinados  de trabajadores que presentan un riesgo mayor, que puede estar relacionado con su función dentro de la empresa o un mayor riesgo de salud o adicción.</p>
</div></td>
    <td width="33%" valign="top"><div class="alert alert-error" style="width:260px">
  <h4 style="font-weight:bold">Prevención indicada:  </h4>
  <p style="color:#b94a48;">Va dirigida a subgrupos concretos de la población con conductas problemáticas y donde ya se han establecido una relación problemática con las drogas.<br />
    <br />
  </p>
</div></td>
  </tr>
</table>
<p class="alert alert-info">La empresa debe acreditar por medio de registros firmados, que sus trabajadores de acuerdo a los roles de cargo, han participado de estas actividades con cierta regularidad. La capacitación considera: la formación del Responsable de la política de la empresa (RPE), charla de Política de A&D y sus procedimientos, seminarios o charlas de prevención del consumo de A&D, actividades sobre temas específicos de prevención en formato charla, obra de teatro, testimonial u otro.</p>