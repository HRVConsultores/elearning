  <h2><span class="label label-success" style="background-color:#8DC15C">3.- Capacitación en prevención del consumo de A&D </span></h2>
  <p><em><img src="<?php echo base_url(IMAGES_U."m01/p_003.jpg") ?>" width="150" height="193" class="foto_der" style="margin-top:17px;" /></em></p>
  <p>Este módulo considera el diseño y  ejecución de un programa de capacitación en prevención del consumo de A&amp;D  como una actividad de prevención universal y selectiva, de acuerdo a los  lineamientos establecidos en la política y los factores de riesgo asociados a la  empresa. </p>
<p><img src="<?php echo base_url(IMAGES_U."m01/07.jpg") ?>" width="450" height="244" class="foto_iz_borderless" />En la actualidad se ha introducido una  nueva clasificación propuesta por el NIDA (que centra su interés en la población a  la que se dirige la acción preventiva).   </p>
