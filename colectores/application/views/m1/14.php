  <h2><span class="label">4.-</span> Ética, confidencialidad y responsabilidades del colector en el protocolo de testeo de A&D. 
</h2>
<img src="<?php echo base_url(IMAGES_U."m01/14.png") ?>" alt="Programa" width="264" height="420" class="foto_der_borderless" />
  <p>El colector es responsable del protocolo de testeo de A&D, debe garantizar la integridad del proceso de colección y de entregar transparencia y seguridad en el manejo de las muestras.<br />
<br />
Debido al tipo de examen que realizará el colector, es importante mencionar que éste deberá mantener absoluta confidencialidad con respecto a datos y resultados obtenidos durante el proceso de testeo de A&D. Toda esta información obtenida o entregada por parte del donante, está protegida por la <strong>Ley 19.628</strong> sobre protección de datos personales, por lo que es fundamental no comentar ni difundir información a terceros. Cualquier resultado o anomalía en el proceso, solo podrá ser informada al RPE de la empresa, quedando bajo su responsabilidad la divulgación de dicha información. <br />
</p>
  <p class="alert alert-info" style="margin-top:60px;">Queda estrictamente  prohibido recibir cualquier tipo de obsequios, propinas u objetos que puedan  inducir a tratar de mejorar o cambiar algún resultado obtenido durante el  proceso de testeo.  </p>
