  <h2><span class="label label-success" style="background-color:#D97F70">5.- Testeo del consumo de A&D </span></h2>

  <p><em><img src="<?php echo base_url(IMAGES_U."m01/p_005.jpg") ?>" width="150" height="193" class="foto_der" style="margin-top:17px;" /></em></p>
  <p>Este módulo incorpora los protocolos técnicos y procedimientos necesarios para la correcta realización de los test de A&D, de acuerdo al estándar para el ámbito laboral empleado en EEUU y adaptado a la legislación chilena. 
 
</p>
<p><img src="<?php echo base_url(IMAGES_U."m01/09.jpg") ?>" width="450" height="244" class="foto_iz_borderless" />El Programa DFWP considera como requisito que la empresa tenga definido en su política de A&D y documentos legales relacionados, la realización de test de A&D, tanto pre-ocupacionales como ocupacionales, de acuerdo al alcance definido para trabajadores propios y contratistas, así como, frecuencia, método de sorteo, características y tamaño de la muestra, coberturas geográficas, razones del testeo y empresa que dará el servicio.
