  <h2>El programa DFWP está acreditado a nivel Internacional</h2>
<p><strong>DFWP</strong>&nbsp;es  un programa&nbsp;<strong>acreditado por IDATIA</strong>&nbsp;(International  Drug  &amp; Alcohol Testing Industry  Association)&nbsp;de <strong>EE.UU</strong>,  transformándose en el<strong>&nbsp;primer  programa de esta índole en Latinoamérica&nbsp;&nbsp;que cumple con los estándares definidos  por esta organización</strong>, líder en la promoción de lugares de  trabajo libres de alcohol y drogas en EEUU. Además, es el primero en el mundo  en estar&nbsp;<strong>certificado bajo la Norma ISO 9001:  2008</strong>,  garantizando así la calidad de sus procesos.</p>
