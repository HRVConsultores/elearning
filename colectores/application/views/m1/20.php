<p>La clasificación de las drogas que  nos interesa conocer en esta oportunidad es según el efecto sobre el Sistema  Nervioso Central (SNC), pero antes   recordaremos brevemente como está formado el sistema nervioso central y  como se transmite la información.</p>

<img src="<?php echo base_url(IMAGES_U."m01/32.png") ?>" alt="Programa" width="320" height="258" class="foto_der_borderless" /><p class="alert alert-info" style="margin-top:20px;">De manera simple podemos decir que el&nbsp;sistema  nervioso central &nbsp;es una red de tejidos&nbsp;cuya  unidad básica son las  neuronas. Su  función primordial es la de captar y procesar rápidamente las señales  ejerciendo control y coordinación sobre los demás órganos para lograr una  adecuada, oportuna y eficaz interacción con el medio ambiente.
</p>
<p>Las neuronas: reciben e integran  información y envían señales a otras células del  cuerpo  a través de <strong>contactos sinápticos.</strong></p>
<ul>
  <li>El  <strong>soma</strong> o  cuerpo, se  lleva a cabo la integración de toda la información obtenida en las dendritas.</li>
  <li>Las <strong>dendritas</strong> actúan como antenas que reciben los contactos de otras células.</li>
  <li>El  <strong>axón</strong> transmite a otras células el mensaje resultante de la integración. </li>
</ul><br />
