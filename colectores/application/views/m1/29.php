<h2>Dependencia a sustancias psicoactivas</h2>
<img src="<?php echo base_url(IMAGES_U."m01/42.png") ?>" alt="Programa" width="418" height="281" class="foto_der_borderless" />
<p>La dependencia a sustancias psicoactivas es la necesidad manifiesta de ingerir la sustancia para aliviar los efectos de su privación, como la inquietud, decaimiento, ansiedad, insomnio, otros síntomas biológicos.<br />
  </p>
<ul>
  <li><strong>La dependencia psíquica </strong>es la  tendencia impulsiva a consumir y continuar   consumiendo una SPA, para sentir la sensación de satisfacción que ésta produce.<br />
    <br />
  </li>
  <li><strong>La dependencia física </strong>es la necesidad biológica por consumir la droga, producto de la adaptación del organismo a una cierta cantidad de ella para funcionar normalmente.<br />
    <br />
  </li>
</ul>

