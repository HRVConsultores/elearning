  <h2><span class="label label-success" style="background-color:#fc9a53">4.- Programa de ayuda al trabajador (PAT) </span></h2>

  <p><em><img src="<?php echo base_url(IMAGES_U."m01/p_004.jpg") ?>" width="150" height="193" class="foto_der" style="margin-top:17px;" /></em></p>
  <p>El PAT considera la implementación de todas las medidas definidas en la política de A&D y que involucrarán a los trabajadores que adhieran a ella. Este módulo incorpora además, el desarrollo de los procedimientos asociados para que los trabajadores puedan acceder en forma  homóloga a todas las enunciaciones de la política. <br />
En este módulo además se incorpora y adapta a nuestra realidad el concepto del SAP (Substance Abuse Profesional), que establece el estándar de EEUU y que hemos denominado para efectos adaptativos del programa como "Profesional especialista en adicciones" (PEA).  
</p>
<p><img src="<?php echo base_url(IMAGES_U."m01/08.jpg") ?>" width="450" height="244" class="foto_iz_borderless" />El PEA es la persona que evalúa  la situación del trabajador que solicita ayuda o es sorprendido transgrediendo la política y ejecuta las medidas proponiendo recomendaciones concernientes a la educación, tratamientos, seguimientos y cuidados posteriores. </p><br />
