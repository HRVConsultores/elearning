  <h2><span class="label label-success" style="background-color:#a68fac">2.- Comunicación, sensibilización y adhesión a la política de A&D </span></h2>
  <p><em><img src="<?php echo base_url(IMAGES_U."m01/p_002.jpg") ?>" width="150" height="193" class="foto_der" style="margin-top:17px;" /></em></p>
  <p>Este módulo  fue diseñado con el objetivo de difundir e involucrar a todos los estamentos de  la empresa en la implementación y ejecución del Programa y de la política de  prevención de A&amp;D definida por la empresa, desde los cargos directivos  hasta las jefaturas y trabajadores. El propósito es generar medios de  comunicación efectivos además de reforzar y alinear aquellos que ya existen de  acuerdo a la socio cultura de la empresa para que tanto el programa como la  política forme parte de la cultura de la empresa.
De esta  forma se espera que el programa sea percibido desde una perspectiva  constructiva y de protección de la salud y seguridad de todos los trabajadores,  incluso para sus familias.</p>
<p>. </p>
<p><img src="<?php echo base_url(IMAGES_U."m01/06.jpg") ?>" width="400" height="217" class="foto_iz_borderless" />En concreto  este módulo considera el diseño y ejecución de un plan de comunicaciones que  permita por una parte, sensibilizar a los trabajadores sobre los beneficios de  una política de A&amp;D y adicionalmente comunicarles sus principales hitos.  Considera finalmente además, la firma de adhesión de los trabajadores a la  Política de A&amp;D.</p>
