  <h2><span class="label label-success" style="background-color:#4a70b1">1.- Diseño de Política de prevención del consumo de A&D </span></h2>

  <p><em><img src="<?php echo base_url(IMAGES_U."m01/p_001.jpg") ?>" width="150" height="193" class="foto_der" style="margin-top:17px;" /></em></p>
  <p>Una Política en cualquier empresa o Institución no es otra cosa que un documento que expone sus principios y lineamientos generales frente a una determinada temática y cómo la abordará de acuerdo a su realidad específica, con relación a aspectos valóricos, culturales, sociales, operacionales, geográficos y de costos entre otros, constituyendo una guía y norma a cumplir para todos los trabajadores independiente del nivel que tengan en la organización. 
</p>
  <p><img src="<?php echo base_url(IMAGES_U."m01/05.jpg") ?>" width="450" height="224" class="foto_iz_borderless" />Adicionalmente y complementario a una política están los procedimientos, que describen en forma detallada los pasos a seguir frente a determinadas situaciones que ella aborda, como por ejemplo procedimientos para contratistas, resultado positivo, custodia y confidencialidad, marcha blanca, testeo pre-ocupacional y ocupacional, seguimiento de enfermedades y tratamientos, etc.</p><br />
