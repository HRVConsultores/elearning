  <h2><span class="label">6.-</span> Conceptos técnicos de alcohol y drogas</h2>
<p>Para una mejor comprensión del efecto de las drogas en el organismo conoceremos los diferentes tipos de sustancias psicotrópicas, su clasificación, efectos sobre el sistema nervioso central y las consecuencias sobre el organismo y la conducta de quienes las consumen.
</p>
<img src="<?php echo base_url(IMAGES_U."m01/31.png") ?>" alt="Programa" width="300" height="301" class="foto_der_borderless" /><p class="alert alert-info" style="margin-top:20px;"><strong>Definición de drogas:</strong>  Para efectos de esta política debe entenderse por "droga", lo establecido en la Ley 20.000 y la definición por la Organización Mundial de la Salud.  La ley 20.000 define como drogas, estupefacientes o sicotrópicos a toda sustancias productoras de dependencia física o síquica, capaces de provocar graves efectos tóxicos o daños considerables a la salud.
</p>
<p>Adicionalmente,  la Organización Mundial de la Salud (OMS), define que debe entenderse por  &ldquo;droga&rdquo;, toda sustancia que, introducida en el organismo por cualquier vía de  administración, produce una alteración del natural funcionamiento del sistema  nervioso central del individuo y es además, susceptible de crear dependencia,  ya sea psicológica, física o ambas.</p>
