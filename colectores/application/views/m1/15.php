  <h2>Roles y responsabilidades del Colector </h2>
<img src="<?php echo base_url(IMAGES_U."m01/15.png") ?>" alt="Programa" width="264" height="420" class="foto_iz_borderless" />
  <p>
  <fieldset class="fieldset">
        <legend style="font-size: 15px;">Administrativas</legend>
        <ol>
  <li>Asegurarse de tener todos los insumos/equipamiento necesario para la toma de muestra. <br />
    Realice un check list : (<a href="<?php echo base_url().DOC; ?>Check_List_material_testeo.docx" target="_blank">Check list material de testeo</a>).</li>
  <li>Recibir al postulante o trabajador (ver más adelante procedimiento de testeo).</li>
  <li>Atender con amabilidad a la persona a testear, ser cordial, educado y mostrar empatía.<br />
    Siempre mantener un trato formal.</li>
  <li>Uso de uniforme, delantal blanco, limpio y abotonado.</li>
  <li>Verificar que los datos de la empresa y trabajador o postulante sean los correctos.</li>
  </ol>
  </fieldset>
  <fieldset class="fieldset">
        <legend style="font-size: 15px;">Del procedimiento</legend>
        <ol>
  <li>Explicar al trabajador o postulante en qué consiste el testeo. <br />
    Obtener su consentimiento para realizarse el testeo e informar los resultados a la empresa;  informar sobre las drogas que serán controladas y la inmediatez y comunicación de los resultados.</li>
  <li>Asistir al postulante o trabajador en la forma correcta de llenar formulario de testeo.</li>
  <li>Verificar que el baño donde se recolecta la muestra cumplan con las condiciones mínimas establecidas en el programa DFWP. (anexo procedimiento de testeo).</li>
  <li>Custodiar el proceso de obtención de muestras acompañando al donante al baño para obtener las muestras.</li>
  </ol>
  </fieldset>
  <fieldset class="fieldset">
        <legend style="font-size: 15px;">Toma de muestra</legend>
        <ol>
  <li>Verificar en una primera instancia que las muestras no han sido manipuladas ni adulteradas.</li>
  <li>Activar el dispositivo y leer los resultados.</li>
  <li>Comunicar los resultados a la persona testeada en forma inmediata.</li>
  <li>Obtener la firma e impresión dactilar del donante en los registros involucrados en el proceso.</li>
  <li>Eliminar correctamente las muestras de orina.</li>
  </ol>
  </fieldset>
  <fieldset class="fieldset" style="padding-left: 274px;">
        <legend style="font-size: 15px;">En los resultados</legend>
        <ol>
  <li>Iniciar la cadena de custodia en caso de ser necesario.</li>
  <li>Ingresar los resultados del testeo a los software.</li>
  <li>Emitir los resultados del testeo en los registros definidos para ese efecto.</li>
  <li>Verificar que los resultados del testeo sean enviados al RPE de la empresa cliente.<br />
  .</li>
  </ol>
  </fieldset>
</ol>
