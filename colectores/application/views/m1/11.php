  <h2><span class="label label-success" style="background-color:#AD2912">6.- Evaluación del Programa DFWP </span></h2>

  <p><em><img src="<?php echo base_url(IMAGES_U."m01/p_006.jpg") ?>" width="150" height="193" class="foto_der" style="margin-top:17px;" /></em></p>
  <p>Este módulo considera la evaluación del Programa DFWP y el impacto que este ha tenido para la empresa, lo cual es medido a través de indicadores de implementación, gestión de resultados y económicos.
 <br />
Es fundamental la evaluación del programa de actividades de cada uno de los módulos del programa y el cumplimiento de los objetivos establecidos en la Política de A&D, a través  de los indicadores o KPI definidos. 
Finalmente el resultado de la evaluación económica a través del ROI de la inversión realizada en el Programa DFWP. 
</p>
<p><img src="<?php echo base_url(IMAGES_U."m01/10.jpg") ?>" width="450" height="244" class="foto_iz_borderless" /></p>
<p><strong>En resumen el Programa DFWP hace tres tipos de evaluaciones:</strong></p>
<ul>
  <li type="1">Evaluación del proceso de implementación    </li>
  <li type="1">Evaluación de Gestión, comparando los resultados obtenidos por la aplicación del Programa DFWP con relación a los objetivos definidos en la Política de A&D    </li>
  <li type="1">Evaluación económica, determinando el ROI (retorno sobre la inversión).</li>
</ul><br />
