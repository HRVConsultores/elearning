<div id="m4" class="simple_overlay" style="width:750px;">
<h1>Metodología</h1>
             
                <p>El curso se  estructura en unidades. Cada una describe una temática diferenciada en sus  contenidos y actividades pero integrada al logro del objetivo de aprendizaje  del curso.<br />
                  <br />
El curso estará dividido en cinco unidades  temáticas, cuatro de ellas, disponibles vía e-learning, a desarrollar en un  máximo de 20 horas, incluidas el tiempo de evaluación final del curso.&nbsp;<br />
<br />
El contenido y las actividades están disponibles  en un servidor web y son accedidas bajo demanda de los propios usuarios.  Quienes participan de este curso aprenden a su propio ritmo sin el apoyo de un  tutor coordinador.<br />
<br />
Para poder navegar en una unidad es muy  importante que en cada contenido revisen el objetivo de la unidad al que hace  referencia (lo encontrarán en la barra superior de cada página, pasando el mouse por sobre la palabra objetivo)</p>
                <p>                  Al culminar la última unidad deberán realizar  una evaluación final que se debe aprobar con un mínimo de 85% de logro en un  máximo de tres intentos. Dicha evaluación consta de 20 pregunta que son  seleccionadas aleatoriamente de un banco de 50 preguntas posibles, por lo que  cada vez que realice la evaluación tanto las preguntas como el orden de las  alternativas será distinto.</p>
  <p>La quinta unidad de la capacitación se realizará de forma presencial en  una jornada de ocho horas, en lugar y fecha a acordar entre las partes.</p>
