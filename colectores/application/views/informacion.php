        <h1>Carta de Bienvenida</h1>
        <p><strong>Global Partners&nbsp;</strong>te da la más cordial  bienvenida a este curso de Certificación de Colectores. Esta certificación te permitirá realizar test  de alcohol y drogas, bajo el estándar establecido en el Programa DFWP. Te  deseamos mucho éxito en el desarrollo del mismo.</p>
         <p>           A modo de información, te  podemos comentar que nuestra empresa desde el año 2001 ha trabajado en la  definición de estándares e implementación de las mejores prácticas en las  técnicas de testeo de A&amp;D, así como también en el desarrollo de programas y  estrategias de intervención en el contexto laboral que prevengan y disminuyan  el consumo de alcohol y drogas en los trabajadores, a través de la  implementación del Programa Drug Free Work Place (DFWP).</p>
         <p>           El Programa DFWP es un  sistema de gestión en prevención del consumo de drogas y abuso del alcohol y se  inspiró en los programas y estándares desarrollados por el gobierno federal de  E.E.U.U, que hace más de 20 años promueve la acreditación de las empresas como  lugares de trabajo libres del A&amp;D, así como en algunos países de la OCDE,  Australia y Nueva Zelandia.</p>
         <p>           El  Programa DFWP es el primero en América Latina<strong>&nbsp;</strong> y está&nbsp;patrocinado&nbsp;por importantes instituciones públicas y  privadas,&nbsp; destacando los<strong>&nbsp;</strong>Ministerios de Minería y  de Transportes, SERNAGEOMIN, Paz Ciudadana<strong>, </strong>etc.&nbsp; Además, el Programa DFWP está acreditado  en EEUU por IDATIA (International Drug and Alcohol Testing Industry  Association).  Adicionalmente, junto con  el Programa DFWP, Global Partners creó una Norma DFWP, similar a una Norma ISO,  que permite a las empresas que hayan implementado exitosamente el Programa  DFWP, certificarse como una Empresa DFWP.<br />
           La metodología utilizada  por el Programa DFWP contempla seis Módulos: Diseño de la política de  prevención de consumo de A&amp;D; Comunicación, sensibilización y adhesión a la  política de A&amp;D; Capacitación en prevención del consumo de A&amp;D;  Programa de ayuda al trabajador; Testeo de A&amp;D y la Evaluación o impacto  del Programa en la organización.<br />
  <br />
           El módulo N° 5, denominado Testeo de A&amp;D, es especialmente importante ya que  considera la implementación de los elementos de control y disuasión del consumo  de alcohol y drogas incorporados en la política de la empresa.</p>
         <p>           De este modo, el curso expuesto a continuación representa la gran experiencia y  el conocimiento, que Global Partners ha recopilado en la última década, sobre  la realización del testeo de alcohol y drogas en el ámbito laboral,  incorporando protocolos técnicos y procedimientos, de acuerdo al estándar  empleado en E.E.U.U. y adaptado a la legislación chilena. </p>
