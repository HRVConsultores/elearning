<img src="<?php echo base_url(IMAGES_U."m02/17.png") ?>" alt="Programa" class="foto_der_borderless" width="350" height="326" /><h2><span class="subtitulo"> Sudor:</span></h2><p>Otro tipo de prueba consiste en detectar la presencia de drogas en el sudor. Una muestra de sudor se recolecta a través de pequeñas almohadillas las cuales son mezcladas con un agua y luego procesadas para verificar inmediatamente la presencia de drogas. 
</p>

<p >Es fácil de recoger y difícil de adulterar o sustituir. Este método muestra presencia actual de las drogas en el donante. Los test de drogas en sudor son de alto costo comparativo a los de orina.
<p class="alert alert-warning" style="margin-top:40px;">El programa DFWP asume el Gold estándar de EEUU para el testeo laboral, por tanto establece como estándar, la muestra de orina para detectar evidencia de consumo reciente de drogas y la exhalación de aire para detectar concentración de alcohol en la sangre.</p>
