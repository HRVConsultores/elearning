<h2><span class="subtitulo"> Fluidos orales: </span></h2><p>
La <strong>saliva</strong> recogida en la boca también puede utilizarse para detectar rastros de drogas y de alcohol. Los líquidos orales son fáciles de recoger (un hisopo en el interior de la mejilla es el método más común), más difíciles de adulterar o sustituir, y puede ser mejor en la detección de determinadas sustancias, incluyendo la marihuana, cocaína y anfetaminas / metanfetaminas. </p>

<img src="<?php echo base_url(IMAGES_U."m02/16.png") ?>" alt="Programa" class="foto_iz_borderless" width="300" height="204" />
<p>Dado que las drogas no se quedan en los fluidos orales por más de dos horas, como si ocurre en la orina, este método muestra presencia actual de las drogas o el alcohol en el donante.
