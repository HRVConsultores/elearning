<h2><img src="<?php echo base_url(IMAGES_U."m02/12.png") ?>" alt="Programa" class="foto_der_borderless" width="363" height="326" /><span class="subtitulo"> Orina:</span></h2><p>Los tests de orina muestran la presencia o ausencia de los metabolitos específicos de las drogas a testear. Los metabolitos son los residuos de las drogas que permanecen en el sistema tiempo después de su consumo.  

</p>
<p class="alert alert-warning" style="margin-top:40px;">Un resultado positivo no señala necesariamente que la persona está bajo la influencia de una droga, sólo determina que hubo consumo en el periodo inmediatamente anterior. 
  <br />

Dado que el alcohol se metaboliza muy rápido en el organismo, aproximadamente 0,15 gr. de alcohol por hora, el análisis de orina para detectar consumo de alcohol debe llevarse a cabo muy rápidamente tras el consumo de alcohol con el fin de garantizar un cierto grado de precisión. Por esta razón, las pruebas de orina no son por lo general útiles en la detección de alcohol. <br/>

