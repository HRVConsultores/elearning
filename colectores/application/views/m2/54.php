  <h2><span class="subtitulo"><span class="label">7.-</span> Normativa legal vigente en Chile para el testeo de A&D y algunos estándares internacionales
</span></h2>
<p>En Chile no existe ninguna norma o protocolo que regule la realización de este tipo de test en el ámbito privado. <br />
En el ámbito público, existe el D.S. 1215 que complementa la Ley 20.000 de drogas. No obstante ello, el Programa DFWP ha incorporado y adaptado, a la realidad del país, recogiendo las mejores prácticas, el protocolo existente en EEUU y que se aplica con éxito hace muchos años.
