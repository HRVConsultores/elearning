<h2><span class="subtitulo"><span class="label">6.-</span> Selección de muestra para testeo laborales ocupacionales</span></h2>
<P><img src="<?php echo base_url(IMAGES_U."m02/20.png") ?>" alt="Programa" class="foto_iz_borderless" width="350" height="326" />

<p>Existen diferentes formas de determinar el grupo de personas que será sometida a testeo, sin embargo cualquiera sea el método seleccionado siempre se debe respetar la legislación vigente del país y las recomendaciones de la dirección del trabajo, todas ellas dirigidas a respetar la privacidad de los trabajadores y la aleatoridad de los exámenes a realizar. <br />
</p>
<p><strong>Los  tipos de testeo a realizar en una empresa pueden ser:</strong></p>
<ul style="position: absolute; left: 40%;">
  <li>Testeo total o censal</li>
  <li>Testeo aleatorio</li>
  <li>Testeo por sospecha fundada</li>
  <li>Testeo pos accidente  o incidente</li>
  <li>Por normativa de  acceso a una empresa</li>
  <li>Por reinserción de un trabajador en tratamiento de  adicciones</li>
</ul>
